public class variablesNumericas {
    public static void main (String[] args) {
        // Mensajes por consola
        System.out.println("Saludos, clase java 2620612");

        // Como declarar variables en java
        // <tipo de dato> + <nombre del dato> + ;

        int A = 8;
        int B = 2;
        int Operaction;

        // Suma
        Operaction = A + B;
        System.out.println("El resultado de la operación de adición es " + Operaction);

        // Resta
        Operaction = A - B;
        System.out.println("El resultado de la operación de resta es " + Operaction);

        // Multiplicación
        Operaction = A * B;
        System.out.println("El resultado de la multiplicación es " + Operaction);

        // División
        Operaction = A / B;
        System.out.println("El resultado de la división es " + Operaction);

    }
}